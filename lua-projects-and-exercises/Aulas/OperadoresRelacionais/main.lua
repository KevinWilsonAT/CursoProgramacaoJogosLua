-- Exemplos de operadores relacionais

-- 1) IGUAL A (==)

local num1 = 10
local num2 = 10

local isEqual = num1 == num2

print("Os números são iguais?", isEqual)

-- 2) DIFERENTE DE (~=)

local color1 = "vermelho"
local color2 = "azul"

local isDifferent = cor1 ~= cor2

print("As cores são diferentes?", isDifferent)

-- 3) MAIOR QUE (>)

local age1 = 25
local age2 = 30

local older = age1 > age2

print("A segunda pessoa é mais velha?", older)

-- 4) MENOR QUE (<)

local distance1 = 100
local distance2 = 150

local shorter = distance1 < distance2

print("A primeira distância é mais curta?", shorter)

-- 5) MAIOR OU IGUAL (>=)

local qty1 = 10
local qty2 = 10

local greatEqual = qty1 >= qty2

print("A primeira qtde. é maior ou igual à segunda?", greatEqual)

-- 6) MENOR OU IGUAL (<=)

local temp1 = 25
local temp2 = 30

local lessEqual = temp1 <= temp2

